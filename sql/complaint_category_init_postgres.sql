-- 4) location 전체 x category 전체 매핑
INSERT INTO location_department (
    location_id,
    department_id,
    location_department_email,
    is_active
)
SELECT
    l.location_id,
    d.department_id,
    lower(
        regexp_replace(d.department_name, '[^0-9A-Za-z가-힣]+', '-', 'g')
    ) || '.' || l.location_id || '@placeholder.local' AS location_department_email,
    TRUE AS is_active
FROM location l
CROSS JOIN department d;

-- complaint_petition.location_department_id FK 복구
DO $$
BEGIN
    IF to_regclass('public.complaint_petition') IS NOT NULL THEN
        EXECUTE '
            ALTER TABLE complaint_petition
            ADD CONSTRAINT fk_complaint_petition_location_department
            FOREIGN KEY (location_department_id)
            REFERENCES location_department(location_department_id)
        ';
    END IF;
END $$;

